    public Main() {
        initComponents();
        setLocationRelativeTo(null); // position frame in the center of the screen
        
        calculator = new Calculator();
        
        resistorColors = new Color[]{
            COLOR_BLACK, COLOR_BROWN, COLOR_RED, COLOR_ORANGE, COLOR_YELLOW,
            COLOR_GREEN, COLOR_BLUE, COLOR_VIOLET, COLOR_GRAY, COLOR_WHITE
        };
        
        resistorColorsText = new String[]{
            "Black", "Brown", "Red", "Orange", "Yellow",
            "Green", "Blue", "Violet", "Gray", "White"
        };
        
        randomResistorValues = new String[]{
            "14k", "47", "33k", "14", "220", 
            "280", "4.7k", "100", "1.2k", "330"
        };
        
        Choice[] bandChoices = {firstbandCHC, secondbandCHC, thirdbandCHC};
        
        for(Choice bandChoice : bandChoices){
            for (String color : resistorColorsText) {
                bandChoice.add(color);
            }
            if (bandChoice == firstbandCHC) {
                bandChoice.remove(resistorColorsText[0]); // first band does not have "Black"
            } else if (bandChoice == thirdbandCHC) {
                bandChoice.remove(resistorColorsText[8]); // third band does not have "Gray"
                bandChoice.remove(resistorColorsText[9]); // third band does not have "White"
            }
        }
        
        inputTF.setText("" + randomResistorValues[new Random().nextInt(randomResistorValues.length)]);
        inputTFKeyPressed(new KeyEvent(this, 1, 1, 1, ENTER_KEY_CODE));
        
    }
    
    private void onResistorColorBandsChange() {
        inputTF.setText("");
        
        int firstBand = firstbandCHC.getSelectedIndex();
        int secondBand = secondbandCHC.getSelectedIndex();
        int thirdBand = thirdbandCHC.getSelectedIndex();
        
        firstbandTF.setBackground(resistorColors[firstBand + 1]); // first band does not have "Black"
        secondbandTF.setBackground(resistorColors[secondBand]);
        thirdbandTF.setBackground(resistorColors[thirdBand]);
        
        resistorValueTF.setText(calculator.getResistorValue(firstBand, secondBand, thirdBand));
    }

