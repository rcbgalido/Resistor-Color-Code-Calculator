    private void inputTFKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inputTFKeyPressed
        if (evt.getKeyCode() == ENTER_KEY_CODE) {
            int[] resistorColorCodes = calculator.getResistorColorCodes(inputTF.getText());
            
            if (resistorColorCodes != null) {
                
                int firstBand = resistorColorCodes[0];
                int secondBand = resistorColorCodes[1];
                int thirdBand = resistorColorCodes[2];
                
                firstbandCHC.select(firstBand - 1); // first band does not have "Black"
                secondbandCHC.select(secondBand);
                thirdbandCHC.select(thirdBand);
                
                firstbandTF.setBackground(resistorColors[firstBand]);
                secondbandTF.setBackground(resistorColors[secondBand]);
                thirdbandTF.setBackground(resistorColors[thirdBand]);
                
                resistorValueTF.setText(calculator.simplifyResistorValue(inputTF.getText()));
                inputTF.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Invalid resistor value!", "Message", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_inputTFKeyPressed

